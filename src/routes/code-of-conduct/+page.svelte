	<h1 class="mt-20 py-6 text-3xl font-bold">CODE OF CONDUCT</h1>
	<div>
		<p class="pb-4">
			PyCon NL is a community conference intended for networking and collaboration in the developer
			community.
		</p>
		<p class="pb-4">
			We value the participation of each member of the Python community and want all participants to
			have an enjoyable and fulfilling experience. Accordingly, all attendees are expected to show
			respect and courtesy to other attendees throughout the conference and at all conference
			events.
		</p>
		<p class="pb-4">
			To make clear what is expected, all staff, attendees, speakers, exhibitors, organisers, and
			volunteers at any PyCon NL event are required to conform to the following Code of Conduct.
			Organisers will enforce this code throughout the event.
		</p>
	</div>

	<h2 class="py-6 text-3xl">OUR COMMUNITY</h2>
	<div class="pb-4">
		Members of the Python community <span class="px-1 font-bold"
			>are open, considerate, and respectful.</span
		> Behaviors that reinforce these values contribute to a positive environment, and include:
	</div>
	<ul class="pl-14">
		<li class="pb-4">
			<span class="px-1 font-bold">Being open.</span> Members of the community are open to collaboration.
		</li>
		<li class="pb-4">
			<span class="px-1 font-bold">Focusing on what is best for the community.</span> We’re respectful
			of the processes set forth in the community, and we work within them.
		</li>
		<li class="pb-4">
			<span class="px-1 font-bold">Acknowledging time and effort.</span> We’re respectful of the volunteer
			efforts that permeate the Python community. We’re thoughtful when addressing the efforts of others,
			keeping in mind that oftentimes the labour was completed simply for the good of the community.
		</li>
		<li class="pb-4">
			<span class="px-1 font-bold">Showing empathy towards other community members.</span> We’re attentive
			in our communications, whether in person or online, and we’re tactful when approaching differing
			views.
		</li>
		<li class="pb-4">
			<span class="px-1 font-bold">Being considerate.</span> Members of the community are considerate
			of their peers.
		</li>
		<li class="pb-4">
			<span class="px-1 font-bold">Being respectful.</span> We’re respectful of others, their positions,
			their skills, their commitments, and their efforts. We’re receptive to constructive comments and
			criticism, as the experiences and skill sets of other members contribute to the whole of our efforts.
		</li>
		<li class="pb-4">
			<span class="px-1 font-bold">Gracefully accepting constructive criticism.</span> When we disagree,
			we are courteous in raising our issues.
		</li>
		<li class="pb-4">
			<span class="px-1 font-bold">Using welcoming and inclusive language.</span> We’re accepting of
			all who wish to take part in our activities, fostering an environment where anyone can participate
			and everyone can make a difference.
		</li>
	</ul>

	<h2 class="py-6 text-3xl">OUR STANDARDS</h2>
	<div class="pb-4">
		Every member of our community has the right to have their identity respected. The Python
		community is dedicated to providing a positive experience for everyone, regardless of age,
		gender identity and expression, sexual orientation, disability, physical appearance, body size,
		ethnicity, nationality, race, religion (or lack thereof), education, cultural and cognitive
		variations, or socio-economic status.
	</div>
	<h2 class="py-6 text-3xl">INAPPROPRIATE BEHAVIOR</h2>
	<div class="pb-4">Examples of unacceptable behaviour by participants include:</div>
	<ul class="pl-14">
		<li class="pb-4">Harassment of any participants in any form</li>
		<li class="pb-4">Deliberate intimidation, stalking, or following</li>
		<li class="pb-4">Logging or taking screenshots of online activity for harassment purposes</li>
		<li class="pb-4">
			Publishing others’ private information, such as a physical or electronic address, without
			explicit permission
		</li>
		<li class="pb-4">Violent threats or language directed against another person</li>
		<li class="pb-4">
			Incitement of violence or harassment towards any individual, including encouraging a person to
			commit suicide or to engage in self-harm
		</li>
		<li class="pb-4">
			Creating additional online accounts in order to harass another person or circumvent a ban
		</li>
		<li class="pb-4">
			Language and imagery in online communities or in any conference venue, including talks, that
			sexualises, insults, demeans, or attacks groups or specific individuals
		</li>
		<li class="pb-4">
			Insults, put downs, or jokes that are based upon stereotypes, that are exclusionary, or that
			hold others up for ridicule
		</li>
		<li class="pb-4">Unwelcome sexual attention or advances</li>
		<li class="pb-4">
			Unwelcome physical contact, including simulated physical contact (e.g., textual descriptions
			like “hug” or “backrub”) without consent or after a request to stop
		</li>
		<li class="pb-4">
			Pattern of inappropriate social contact, such as requesting/assuming inappropriate levels of
			intimacy with others
		</li>
		<li class="pb-4">
			Sustained disruption of online community discussions, in-person presentations, or other
			in-person events
		</li>
		<li class="pb-4">Continued one-on-one communication after requests to cease</li>
		<li class="pb-4">
			Other conduct that is inappropriate for a professional audience including people of many
			different backgrounds
		</li>
	</ul>
	<div class="py-4">
		Community members asked to stop any inappropriate behaviour are expected to comply immediately.
	</div>
	<h3 class="py-6 text-3xl">BE PROFESSIONAL AND DON'T SPAM</h3>
	<div class="pb-4">
		We consider PyCon NL a great setting to showcase the work of the community and would like to
		encourage networking and business related discussions, as well as keep the conference
		presentations meaningful and interesting for everyone.
	</div>
	<div class="pb-4">We therefore:</div>
	<ul class="pl-14">
		<li class="pb-4">
			primarily make company marketing, promotional or recruiting related activities at the
			conference available to our paid sponsors, unless agreed otherwise;
		</li>
		<li class="pb-4">
			request presentations - outside the official recruiting sessions - to focus on Python related
			topics, not on recruitment.
		</li>
	</ul>
	<div class="py-4">
		The PyCon NL organisation will be happy to answer any questions related to marketing or
		recruiting activities.
	</div>
	<h2 class="py-6 text-3xl">CONSEQUENCES</h2>
	<div class="pb-4">
		If a participant engages in behaviour that violates this Code of Conduct, the PyCon NL
		organization team may take action they deem appropriate, including warning the offender or
		expulsion from the community and community events with no refund of event tickets.
	</div>
	<div class="pb-4">
		Thank you for helping make this a welcoming, friendly community for everyone.
	</div>
	<h2 class="py-6 text-3xl">SCOPE</h2>
	<h3 class="pb-4 text-xl">PYCON NL EVENTS</h3>
	<div class="pb-4">This Code of Conduct applies to the following people at PyCon NL:</div>
	<ul class="pl-14">
		<li class="pb-4">staff</li>
		<li class="pb-4">speakers</li>
		<li class="pb-4">panellists</li>
		<li class="pb-4">tutorial or workshop leaders</li>
		<li class="pb-4">poster presenters</li>
		<li class="pb-4">people invited to meetings or summits</li>
		<li class="pb-4">exhibitors</li>
		<li class="pb-4">organisers</li>
		<li class="pb-4">volunteers</li>
		<li class="pb-4">sponsors</li>
		<li class="pb-4">all attendees</li>
	</ul>
	<div class="pb-4 pt-6">
		The Code of Conduct applies in official venue event spaces, including:
	</div>
	<ul class="pl-14">
		<li class="pb-4">exhibit hall or vendor tabling area</li>
		<li class="pb-4">panel and presentation rooms</li>
		<li class="pb-4">hackathon or sprint rooms</li>
		<li class="pb-4">tutorial or workshop leaders</li>
		<li class="pb-4">poster session rooms</li>
		<li class="pb-4">summit or meeting rooms</li>
		<li class="pb-4">staff areas</li>
		<li class="pb-4">meal areas</li>
		<li class="pb-4">party suites</li>
		<li class="pb-4">
			walkways, hallways, elevators, and stairs that connect any of the above spaces
		</li>
	</ul>
	<div class="py-4">
		The Code of Conduct applies to interactions with official event accounts on social media spaces
		and phone applications, including:
	</div>
	<ul class="pl-14">
		<li class="pb-4">comments made on official conference phone apps</li>
		<li class="pb-4">comments made on event video hosting services</li>
		<li class="pb-4">comments made on the official event hashtag or panel hashtags</li>
	</ul>
	<div class="py-4">Event organizers will enforce this code throughout the event.</div>
