	<h2 class="py-6 text-3xl">PROCEDURE FOR HANDLING INCIDENTS</h2>
	<div class="pb-4">
		When the organization team receives a report of a possible Code of Conduct violation, it will:
	</div>
	<ol class="pl-14">
		<li class="pb-4">Acknowledge the receipt of the report.</li>
		<li>Evaluate conflicts of interest.</li>
		<li class="pb-4">
			Call a meeting with the organization team who do not have a conflict of interest.
		</li>
		<li class="pb-4">Evaluate the reported incident.</li>
		<li class="pb-4">Propose a behavioral modification plan.</li>
		<li class="pb-4">Propose consequences for the reported behavior.</li>
		<li class="pb-4">
			Vote on behavioral modification plan and consequences for the reported person.
		</li>
		<li class="pb-4">
			Contact online community administrators/moderators to approve the behavioral modification plan
			and consequences.
		</li>
		<li class="pb-4">Follow up with the reported person.</li>
		<li class="pb-4">Decide further responses.</li>
		<li class="pb-4">Follow up with the reporter.</li>
	</ol>
